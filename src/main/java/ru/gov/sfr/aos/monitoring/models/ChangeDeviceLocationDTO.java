package ru.gov.sfr.aos.monitoring.models;

/**
 *
 * @author 041AlikinOS
 */
public class ChangeDeviceLocationDTO {
    
    public Long id;
    public String location;

    public ChangeDeviceLocationDTO(Long id, String location) {
        this.id = id;
        this.location = location;
    }

    public ChangeDeviceLocationDTO() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }
    
    
    
}
